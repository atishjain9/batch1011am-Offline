


public class Pattern1
{
	public static  void main(String[] args)
	{

    //nested loop
	for(int r=1;r<=5;r++) //outer loop
	{
		for(int c=1;c<=r;c++) //inner loop
		{
			System.out.print(" * ");
		}

        System.out.println();
	}
		

	}
}



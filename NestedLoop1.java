


public class NestedLoop1
{
	public static  void main(String[] args)
	{

    //nested loop
	for(int r=1;r<=5;r++)
	{
		for(int c=1;c<=5;c++)
		{
			System.out.print(" * ");
		}

        System.out.println();
	}
		

	}
}



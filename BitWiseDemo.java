

public class BitWiseDemo
{
	static public  void main(String[] args)
	{
		int a = 25;
		int b = 15;

		int no = 12;

		System.out.println("a & b:"+(a&b));
		System.out.println("a & b:"+(a|b));
		System.out.println("a & b:"+(a^b));
		

		System.out.println("~no:"+(~no));
	}
}


